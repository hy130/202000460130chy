u32 loopleft(u32 A1, int n) {//循环左移函数，操作符移位是自动补零
	u32 A = A1;
	for (int i = 0; i < n; i++) {
		if (A / 0x80000000 == 1) {//最左边为1时，后面加1
			A = A * 2;
			A = A + 1;
		}
		else {
			A = A * 2;
		}
	}
	return A;
}

u32 T_function(u32 A) {//密钥生成里的T函数
	u32 B1;
	u32 B2;
	u32 result;
	u8 result_Sbox[4];
	u8 a[4];
	short i;
	a[0] = A / 0x1000000;//分为4个8bit块
	a[1] = A / 0x10000;
	a[2] = A / 0x100;
	a[3] = A;
	u32 temp_ = Sbox[a[0]] * 256 * 256 * 256 + Sbox[a[1]] * 256 * 256 + Sbox[a[2]] * 256 + Sbox[a[3]];//进S盒
	B1 = loopleft((u32)temp_, 13);
	B2 = loopleft((u32)temp_, 23);
	result = (B1 ^ B2 ^ temp_);//异或之后将结果返回
	return result;
}
u32 T_function1(u32 A) {//加密函数里的T函数
	u32 B1;
	u32 B2;
	u32 B3;
	u32 B4;
	u32 result;
	u8 result_Sbox[4];
	result_Sbox[0] = Sbox[u8(A / 0x1000000)];//分别进入S盒
	result_Sbox[1] = Sbox[u8(A / 0x10000)];
	result_Sbox[2] = Sbox[u8(A / 0x100)];
	result_Sbox[3] = Sbox[u8(A)];
	u32 temp = result_Sbox[0] * 256 * 256 * 256 + result_Sbox[1] * 256 * 256 + result_Sbox[2] * 256 + result_Sbox[3];
	B1 = loopleft((u32)temp, 2);
	B2 = loopleft((u32)temp, 10);
	B3 = loopleft((u32)temp, 18);
	B4 = loopleft((u32)temp, 24);
	result = (B1 ^ B2 ^ B3 ^ B4 ^ temp);//异或后返回
	return result;
}
void getkeys() {//密钥生成函数
	u32 temp[4];
	u8 keyFk_temp[4];
	for (int i = 0; i < 4; i++) {
		keyFk_temp[0] = key[0 + 4 * i];//读取数据
		keyFk_temp[1] = key[1 + 4 * i];
		keyFk_temp[2] = key[2 + 4 * i];
		keyFk_temp[3] = key[3 + 4 * i];
		temp[i] = (keyFk_temp[0] * 256 * 256 * 256 + keyFk_temp[1] * 256 * 256 + keyFk_temp[2] * 256 + keyFk_temp[3]) ^ FK[i];
	}
	for (int i = 0; i < 32; i++) {//进行32步循环
		u32 temp1 = CK[i] ^ temp[1] ^ temp[2] ^ temp[3];
		rk[i] = T_function(temp1) ^ temp[0];
		for (int i = 0; i < 3; i++) {
			temp[i] = temp[i + 1];
		}
		temp[3] = rk[i];
		//cout << hex << rk[i] << endl;
	}
}
u32* encrypt(u32 m[4]) {//加密函数
	getkeys();
	for (int i = 0; i < 32; i++) {//32次循环，分别需要与对应的密钥进行异或
		u32 temp_X = T_function1(m[1] ^ m[2] ^ m[3] ^ rk[i]) ^ m[0];
		for (int i = 0; i < 3; i++) {
			m[i] = m[i + 1];
		}
		m[3] = temp_X;
	}
	u32* result;
	result = (u32*)malloc(sizeof(u32) * 4);
	for (int i = 0; i < 4; i++) {//在最后调换一下次序
		result[i] = m[3 - i];
	}
	return result;
}

